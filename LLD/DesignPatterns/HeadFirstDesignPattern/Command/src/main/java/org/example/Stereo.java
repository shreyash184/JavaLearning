package org.example;

public class Stereo {

    public Stereo(){

    }

    public void on(){
        System.out.println("Stereo is on");
    }

    public void off(){
        System.out.println("Stero is off");
    }

    public void onWithCD(){
        System.out.println("Stero is on with CD");
    }

    public void offWithCD(){
        System.out.println("Stereo is off with CD");
    }
}
